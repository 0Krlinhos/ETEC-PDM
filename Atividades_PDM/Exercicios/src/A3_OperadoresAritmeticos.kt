//A3 Operadores Aritméticos

fun main() {
    // Aula 3: Operadores Aritméticos: ( + - * / % )
    var idade = 20


    idade += 1 // soma (+)


    //idade = idade - 5
    idade -= 5 // subtração (-)


    //idade = idade * 2
    idade *= 2 // multiplicação (*)


    //idade = idade / 3
    idade /= 3 // divisão (/)


    //idade = idade % 3
    idade %= 3 // resto da divisão (%)


    println("Quantos anos tem este edifício? Acho que $idade.")
}
